# Спрашиваем, сколько строк нужно ввес
n = int(input("Введите количество строк: "))

# Если число меньше 1 — предупреждаем и выходим
if n <= 0:
    print("Количество строк должно быть больше нуля!")
else:
    text = ""  # создаём пустую строку для всего текста

    # вводим строки
    for i in range(n):
        s = input(f"Введите строку №{i + 1}: ")
        text += " " + s  # добавляем введённую строку к общему тексту

    # разбиваем текст на слова (split сам уберёт лишние пробелы)
    words = text.split()

    # считаем уникальные слова (set убирает повторы)
    unique_words = set(words)

    # выводим результат
    print("Количество различных слов:", len(unique_words))
